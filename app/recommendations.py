def classify_items(detected_names: List[str]) -> Dict[str, List[str]]:
    grouped = {}
    for name in detected_names:
        cat = CATEGORY_MAP.get(name.lower(), 'other')
        if cat not in ['person', 'other']:
            grouped.setdefault(cat, []).append(name)
    return grouped


def _detect_gender(items: List[str]) -> str:
    female_markers = ['skirt', 'dress', 'blouse', 'tote', 'heels', 'pumps', 'ballet', 'earrings', 'necklace', 'handbag']
    male_markers = ['blazer', 'trousers', 'suit', 'brogues', 'monk', 'oxford', 'tie', 'suspenders', 'belt']
    
    f_count = sum(1 for item in items for m in female_markers if m in item.lower())
    m_count = sum(1 for item in items for m in male_markers if m in item.lower())
    
    if f_count > m_count:
        return 'female'
    elif m_count > f_count:
        return 'male'
    return 'unisex'


def _get_recs_for_item(item_name: str, style: str, gender: str = 'auto', detected_items: List[str] = None) -> Dict[str, List[str]]:
    if gender == 'auto' and detected_items:
        gender = _detect_gender(detected_items)
    
    key = item_name.lower()
    
    if gender == 'male' and key in RECOMMENDATIONS_MALE:
        recs = RECOMMENDATIONS_MALE[key]
    elif gender == 'female' and key in RECOMMENDATIONS_FEMALE:
        recs = RECOMMENDATIONS_FEMALE[key]
    else:
        recs = RECOMMENDATIONS.get(key, {})
    
    return recs.get(style, recs.get('casual', {}))


def generate_outfit(
    detected_items: List[str],
    style: str = 'casual',
    detected_colors: List[str] = None,
    manual_type: str = None,
    randomize: bool = False,
    gender: str = 'auto'
) -> Dict:
    grouped = classify_items(detected_items)
    
    if manual_type:
        cat = CATEGORY_MAP.get(manual_type.lower(), manual_type)
        if cat not in ['person', 'other']:
            grouped.setdefault(cat, [manual_type])
    
    present = list(grouped.keys())
    
    if not present:
        return {
            'detected': detected_items,
            'present': [],
            'missing': OUTFIT_RULES.get(style, OUTFIT_RULES['casual'])[0],
            'suggestions': {},
            'advice': f'Выберите тип одежды вручную для стиля {style}.',
            'color_tips': []
        }
    
    actual_gender = gender if gender != 'auto' else _detect_gender(detected_items)
    
    template = OUTFIT_RULES.get(style, OUTFIT_RULES['casual'])[0]
    missing = [cat for cat in template if cat not in present]
    
    all_names = []
    for names in grouped.values():
        all_names.extend(names)
    
    suggestions = {}
    suggestions_alt = {}
    
    for name in all_names:
        recs = _get_recs_for_item(name, style, actual_gender, detected_items)
        for key, items in recs.items():
            if not items:
                continue
            
            is_alt = 'альтернатива' in key
            base_key = key.replace(' (альтернатива)', '').strip()
            
            cat_map = {
                'верх': 'top', 'низ': 'bottom', 'обувь': 'shoes',
                'верхняя одежда': 'outerwear', 'сумка': 'bag', 'аксессуары': 'accessory'
            }
            target_cat = cat_map.get(base_key, base_key)
            
            if is_alt:
                if target_cat in present:
                    suggestions_alt.setdefault(base_key, []).extend(items)
            else:
                if target_cat not in present and target_cat in missing:
                    suggestions.setdefault(base_key, []).extend(items)
    
    for cat in suggestions:
        unique = list(dict.fromkeys(suggestions[cat]))
        if randomize:
            random.shuffle(unique)
        suggestions[cat] = unique[:4]
    
    for cat in suggestions_alt:
        unique = list(dict.fromkeys(suggestions_alt[cat]))
        if randomize:
            random.shuffle(unique)
        suggestions_alt[cat] = unique[:2] 
    
    final_suggestions = {}
    for cat, items in suggestions.items():
        final_suggestions[cat] = items
    for cat, items in suggestions_alt.items():
        final_suggestions[f'{cat} (альтернатива)'] = items
    
    cat_name_map = {
        'top': 'верх', 'bottom': 'низ', 'shoes': 'обувь',
        'outerwear': 'верхняя одежда', 'bag': 'сумка', 'accessory': 'аксессуары'
    }
    
    base_suggestions = BASE_OUTFIT_SUGGESTIONS_MALE if actual_gender == 'male' else BASE_OUTFIT_SUGGESTIONS_FEMALE
    for miss_cat in missing:
        if miss_cat in base_suggestions:
            cat_recs = base_suggestions[miss_cat].get(style, base_suggestions[miss_cat]['casual'])
            ru_name = cat_name_map.get(miss_cat, miss_cat)
            if ru_name not in final_suggestions:
                final_suggestions[ru_name] = cat_recs[:3]
    
    accessory_items = grouped.get('accessory', [])
    acc_suggestions = []
    
    for acc_name in accessory_items:
        acc_key = acc_name.lower()
        if actual_gender == 'male':
            recs = ACCESSORY_RECOMMENDATIONS_MALE.get(acc_key, [])
        else:
            recs = ACCESSORY_RECOMMENDATIONS_FEMALE.get(acc_key, [])
        if recs:
            acc_suggestions.extend(recs[:3])
    
    if acc_suggestions:
        final_suggestions['аксессуары (дополнительно)'] = list(dict.fromkeys(acc_suggestions))[:4]
    elif 'accessory' not in present:
        if actual_gender == 'male':
            base_acc = ['часы', 'ремень', 'солнечные очки', 'шарф']
        else:
            base_acc = ['серьги', 'ожерелье', 'браслет', 'часы', 'сумка']
        final_suggestions['аксессуары'] = base_acc[:4]
    
    if 'bag' not in present:
        if actual_gender == 'male':
            bag_recs = BAG_RECOMMENDATIONS_MALE.get(style, BAG_RECOMMENDATIONS_MALE['casual'])
        else:
            bag_recs = BAG_RECOMMENDATIONS_FEMALE.get(style, BAG_RECOMMENDATIONS_FEMALE['casual'])
        if 'сумка' not in final_suggestions:
            final_suggestions['сумка'] = bag_recs[:3]
    
    for cat in final_suggestions:
        final_suggestions[cat] = list(dict.fromkeys(final_suggestions[cat]))[:4]
    
    advice = _make_advice(present, missing, style, actual_gender)
    
    return {
        'detected': detected_items,
        'present': present,
        'missing': missing,
        'suggestions': final_suggestions,
        'advice': advice,
        'color_tips': []
    }


def _make_advice(present: List[str], missing: List[str], style: str, gender: str = 'unisex') -> str:
    if not missing:
        return f'Образ в стиле {style} готов!'
    
    missing_str = ', '.join(missing)
    text = f'Для завершенного образа в стиле {style} добавьте: {missing_str}.'
    
    if 'dress' in present:
        text += ' С платьем хорошо сочетаются аксессуары.'
    elif 'top' in present and 'bottom' in present:
        if gender == 'male':
            text += ' Для мужского образа рекомендуются: часы, ремень, классические туфли или броги.'
        elif gender == 'female':
            text += ' Для женского образа рекомендуются: украшения, шарф, сумка, туфли или балетки.'
        else:
            text += ' Также можно добавить аксессуары или заменить верх/низ на альтернативный вариант.'
    
    return text
